# Python program to convert number to decimal number

str1 = "20"
str2 = "1011"
str3 = "1c0"

num1 = int(str1, 8)
print('Octal 20 =',num1)

num2 = int(str2, 2)
print('Binary 1011 = ', num2)

num3 = int(str3, 16)
print('Hexadecimal 1c0 = ', num3)

# Result
#
# Octal 20 = 16
# Binary 1011 =  11
# Hexadecimal 1c0 =  448
