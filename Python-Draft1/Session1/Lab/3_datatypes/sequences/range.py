# Sequences

# Range datatype
# Properties
# 1. The range datatype represents a sequence of numbers.
# 2. The numbers in the range are not modifiable.
# 3. Generally, range is used for repeating a for loop for
#    a specific number of times.

range1 = range(5)

# Range values
for i in range1:
    print(i)

range2 = range(10, 30, 5)

# Range values interval of 5.
for i in range2:
    print(i)

